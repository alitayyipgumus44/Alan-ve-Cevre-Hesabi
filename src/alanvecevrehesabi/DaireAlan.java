package alanvecevrehesabi;

class DaireAlan extends AlanveCevre {

    public void daireAlan(int yaricap) {
        System.out.print("Dairenin Alanı: "+(3.14 * (yaricap * yaricap))+" ");
    }
    
    public void daireCevre(int yaricap) {
        System.out.print("Dairenin Çevresi: "+(2 * 3.14 * yaricap )+" ");
    }
}
